CREATE POLICY "Admin RH pode fazer upload em qualquer pasta"
ON storage.objects FOR INSERT
TO authenticated
WITH CHECK (
    bucket_id = 'contracheques'
    AND (
        auth.jwt() ->> 'email' = 'admin.rh@isiba.com'
        OR auth.jwt() ->> 'email' LIKE '%@isiba.com'
    )
);

