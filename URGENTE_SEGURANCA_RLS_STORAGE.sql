-- 2. POLΓTICA: COLABORADOR VΓ APENAS SUA PASTA
-- β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•β•

CREATE POLICY "Colaborador acessa apenas sua pasta"
ON storage.objects FOR SELECT
TO authenticated
USING (
    bucket_id = 'contracheques' 
    AND (storage.foldername(name))[1] = auth.uid()::text
);

-- ExplicaΓ§Γ£o:
-- - storage.foldername(name) retorna array de pastas no caminho
-- - [1] pega a primeira pasta (UUID do colaborador)
-- - auth.uid() Γ© o ID do usuΓ΅rio autenticado
-- - SΓ³ funciona se o caminho for: contracheques/UUID_DO_COLABORADOR/arquivo.pdf

